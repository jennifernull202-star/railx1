export interface GetNavigationItemsParams {
  isAdmin?: boolean;
  hasContractorProfile?: boolean;
  isVerifiedContractor?: boolean;
  hasSellerSubscription?: boolean;
  unreadMessages?: number;
  pendingInquiries?: number;
  // Dashboard visibility preferences (from user settings)
  showSellerSection?: boolean;
  showContractorSection?: boolean;
}

export function getNavigationItems({
  isAdmin = false,
  hasContractorProfile = false,
  isVerifiedContractor = false,
  hasSellerSubscription = false,
  unreadMessages = 0,
  pendingInquiries = 0,
  showSellerSection = true,
  showContractorSection = true,
}: GetNavigationItemsParams): NavSection[] {
  const sections: NavSection[] = [];

  // Marketplace section - for all users
  sections.push({
    title: 'Marketplace',
    items: [
      { href: '/listings', label: 'Browse Listings', icon: <ListingsIcon /> },
      { href: '/dashboard/saved', label: 'Saved Items', icon: <HeartIcon /> },
      { 
        href: '/dashboard/inquiries', 
        label: 'My Inquiries', 
        icon: <InboxIcon />,
        badge: pendingInquiries > 0 ? pendingInquiries : undefined,
      },
      { href: '/contractors', label: 'Find Contractors', icon: <UsersIcon /> },
    ],
  });

  // Selling section - show to everyone (free listings available)
  if (showSellerSection) {
    sections.push({
      title: 'Selling',
      items: [
        { href: '/listings/create', label: 'Create Listing', icon: <HomeIcon /> },
        { href: '/dashboard/listings', label: 'Manage Listings', icon: <ListingsIcon /> },
        { 
          href: '/dashboard/messages', 
          label: 'View Inquiries', 
          icon: <MessagesIcon />,
          badge: unreadMessages > 0 ? unreadMessages : undefined,
        },
        { href: '/dashboard/upgrade', label: 'Upgrade Plan', icon: <AnalyticsIcon /> },
        { href: '/dashboard/verification/seller', label: 'Get Verified', icon: <ShieldIcon /> },
      ],
    });
  }

  // Contractor section - show to everyone (free directory listing available)
  if (showContractorSection) {
    sections.push({
      title: 'Contractor',
      items: [
        { 
          href: '/dashboard/contractor/profile',
          label: 'Contractor Profile',
          icon: <ProfileIcon />,
        },
        { 
          href: '/dashboard/contractor/services',
          label: 'My Services',
          icon: <ToolsIcon />,
        },
        { 
          href: '/dashboard/contractor/leads', 
          label: 'Service Leads', 
          icon: <LeadsIcon /> 
        },
        {
          href: '/dashboard/contractor/verify',
          label: 'Get Verified',
          icon: <ShieldIcon />,
        },
      ],
    });
  }

  // ISO (In Search Of) section
  sections.push({
    title: 'ISO',
    items: [
      { href: '/iso', label: 'Browse Requests', icon: <ListingsIcon /> },
      { href: '/iso/create', label: 'Create Request', icon: <InboxIcon /> },
    ],
  });

  // Admin section
  if (isAdmin) {
    sections.push({
      title: 'Admin',
      items: [
        { href: '/admin', label: 'Admin Dashboard', icon: <ShieldIcon /> },
        { href: '/admin/users', label: 'Users', icon: <UsersIcon /> },
        { href: '/admin/listings', label: 'Listings', icon: <ListingsIcon /> },
        { href: '/admin/contractors', label: 'Contractors', icon: <ProfileIcon /> },
        { href: '/admin/analytics', label: 'Analytics', icon: <AnalyticsIcon /> },
        { href: '/admin/addons', label: 'Add-on Revenue', icon: <DollarIcon /> },
      ],
    });
  }

  // Account section - for all users
  sections.push({
    title: 'Account',
    items: [
      { href: '/dashboard/billing', label: 'Plans & Billing', icon: <BillingIcon /> },
      { href: '/dashboard/settings', label: 'Settings', icon: <SettingsIcon /> },
    ],
  });

  return sections;
}
